package mx.ine.computosINE.dto.reportes;

import java.io.Serializable;

import com.google.gson.annotations.SerializedName;

public class DTOParametrosEntradaWS implements Serializable{
	
	private static final long serialVersionUID = 220520170749L;
	/**
     * Id Proceso Electoral que se pasa como parametro
     */
    @SerializedName("idProcesoElectoral")
    private Integer idProcesoElectoral;
    
    /**
     * Id del estado que se pasa como parametro
     */
    @SerializedName("idEstado")
    private Integer idEstado;
    
    /**
     * Id de las asociaciones que se pasa como parametro
     * separados por coma
     */
    @SerializedName("idAsociacion")
    private String idAsociacion;

	public Integer getIdProcesoElectoral() {
		return idProcesoElectoral;
	}

	public void setIdProcesoElectoral(Integer idProcesoElectoral) {
		this.idProcesoElectoral = idProcesoElectoral;
	}

	public Integer getIdEstado() {
		return idEstado;
	}

	public void setIdEstado(Integer idEstado) {
		this.idEstado = idEstado;
	}

	public String getIdAsociacion() {
		return idAsociacion;
	}

	public void setIdAsociacion(String idAsociacion) {
		this.idAsociacion = idAsociacion;
	}
    
}
