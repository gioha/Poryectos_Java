package mx.ine.computosINE.dto.reportes;

import java.io.Serializable;

public class DTOEncabezadoAsociaciones implements Serializable {

	private static final long serialVersionUID = 210420170648L;

	private String nombre;
	private Integer idProceso;
	private Integer idDetalleProceso;
	private Integer idAsociacion;
	private Integer idCoalicion;
	private Integer idSubcoalicion;
	private Integer tipoAsociacion;
	private Integer idEstado;
	private Integer idDistrito;
	private Integer idMunicipio;
	private String siglas;
	private Integer orden;
	private Integer votosTotales;
	public String getNombre() {
		return nombre;
	}
	public void setNombre(String nombre) {
		this.nombre = nombre;
	}
	public Integer getIdProceso() {
		return idProceso;
	}
	public void setIdProceso(Integer idProceso) {
		this.idProceso = idProceso;
	}
	public Integer getIdDetalleProceso() {
		return idDetalleProceso;
	}
	public void setIdDetalleProceso(Integer idDetalleProceso) {
		this.idDetalleProceso = idDetalleProceso;
	}
	public Integer getIdAsociacion() {
		return idAsociacion;
	}
	public void setIdAsociacion(Integer idAsociacion) {
		this.idAsociacion = idAsociacion;
	}
	public Integer getIdCoalicion() {
		return idCoalicion;
	}
	public void setIdCoalicion(Integer idCoalicion) {
		this.idCoalicion = idCoalicion;
	}
	public Integer getIdSubcoalicion() {
		return idSubcoalicion;
	}
	public void setIdSubcoalicion(Integer idSubcoalicion) {
		this.idSubcoalicion = idSubcoalicion;
	}
	public Integer getTipoAsociacion() {
		return tipoAsociacion;
	}
	public void setTipoAsociacion(Integer tipoAsociacion) {
		this.tipoAsociacion = tipoAsociacion;
	}
	public Integer getIdEstado() {
		return idEstado;
	}
	public void setIdEstado(Integer idEstado) {
		this.idEstado = idEstado;
	}
	public Integer getIdDistrito() {
		return idDistrito;
	}
	public void setIdDistrito(Integer idDistrito) {
		this.idDistrito = idDistrito;
	}
	public Integer getIdMunicipio() {
		return idMunicipio;
	}
	public void setIdMunicipio(Integer idMunicipio) {
		this.idMunicipio = idMunicipio;
	}
	public String getSiglas() {
		return siglas;
	}
	public void setSiglas(String siglas) {
		this.siglas = siglas;
	}
	public Integer getOrden() {
		return orden;
	}
	public void setOrden(Integer orden) {
		this.orden = orden;
	}
	public Integer getVotosTotales() {
		return votosTotales;
	}
	public void setVotosTotales(Integer votosTotales) {
		this.votosTotales = votosTotales;
	}
	@Override
	public String toString() {
		return "[idAsociacion=" + idAsociacion
				+ ", idCoalicion=" + idCoalicion + ", idSubcoalicion="
				+ idSubcoalicion + ", tipoAsociacion=" + tipoAsociacion
				+ ", siglas=" + siglas + "]";
	}
	
	
}
