package mx.ine.gestion.dto.helper;

import java.io.Serializable;

/**
 * Clase creada para ayudar a la carga de instrucciones turnadas en el historico.
 * 
 * @author Homero Fidel Villanueva
 * @since 05/03/2018
 *
 */
public class DTOCargaTurnadoInstruHelper implements Serializable{
	
	/**
	 * 
	 */
	private static final long serialVersionUID = 2080515610295303329L;

	/**
	 * Atributo que guarda el idDocumento al cual pertenecen las instrucciones
	 */
	private Integer idDocumento;
	
	/**
	 * Atributo que guarda el identificador de recepción 
	 */
	private Integer idHistoricoRecep;
	
	/**
	 * Atributo que guarda el identificador de la persona que realizó el turnado
	 */
	private Integer idPersonaTurno;
	
	/**
	 * Atributo que guarda el identificador de la persona a la que fue turnado el documento. 
	 */
	private Integer idPersonaTurnada;
	
	/**
	 * Atributo que guarda el id del area
	 */
	private Integer idArea;
	
	/**
	 * Atributo que guarda el tipo de Area
	 */
	private Integer tipoArea;
	
	/**
	 * Atributo que guarda el id de la instrucción turnada
	 */
	private Integer idInstruccion;
	
	/**
	 * Atributo que guarda el tipo de instrucción turnada. 1 significa instrucción de atención, 2 significa instrucción informativa
	 */
	private Integer tipoInstruccion;
	
	/**
	 * Atributo que guarda el campo ordenamiento de la tabla "INSTRUCCIONES"
	 */
	private Integer ordenamiento;
	
	/**
	 * @return valor de tipo Integer que esta contenido en la variable idDocumento
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public Integer getIdDocumento() {
		return idDocumento;
	}
	/**
	 * @param idDocumento : valor que se ingresa a la variable de tipo Integer
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public void setIdDocumento(Integer idDocumento) {
		this.idDocumento = idDocumento;
	}
	/**
	 * @return valor de tipo Integer que esta contenido en la variable idHistoricoRecep
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public Integer getIdHistoricoRecep() {
		return idHistoricoRecep;
	}
	/**
	 * @param idHistoricoRecep : valor que se ingresa a la variable de tipo Integer
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public void setIdHistoricoRecep(Integer idHistoricoRecep) {
		this.idHistoricoRecep = idHistoricoRecep;
	}
	/**
	 * @return valor de tipo Integer que esta contenido en la variable idPersonaTurno
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public Integer getIdPersonaTurno() {
		return idPersonaTurno;
	}
	/**
	 * @param idPersonaTurno : valor que se ingresa a la variable de tipo Integer
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public void setIdPersonaTurno(Integer idPersonaTurno) {
		this.idPersonaTurno = idPersonaTurno;
	}
	/**
	 * @return valor de tipo Integer que esta contenido en la variable idInstruccion
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public Integer getIdInstruccion() {
		return idInstruccion;
	}
	/**
	 * @param idInstruccion : valor que se ingresa a la variable de tipo Integer
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public void setIdInstruccion(Integer idInstruccion) {
		this.idInstruccion = idInstruccion;
	}
	/**
	 * @return valor de tipo Integer que esta contenido en la variable idPersonaTurnada
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public Integer getIdPersonaTurnada() {
		return idPersonaTurnada;
	}
	/**
	 * @param idPersonaTurnada : valor que se ingresa a la variable de tipo Integer
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public void setIdPersonaTurnada(Integer idPersonaTurnada) {
		this.idPersonaTurnada = idPersonaTurnada;
	}
	/**
	 * @return valor de tipo Integer que esta contenido en la variable tipoInstruccion
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public Integer getTipoInstruccion() {
		return tipoInstruccion;
	}
	/**
	 * @param tipoInstruccion : valor que se ingresa a la variable de tipo Integer
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public void setTipoInstruccion(Integer tipoInstruccion) {
		this.tipoInstruccion = tipoInstruccion;
	}
	/**
	 * @return valor de tipo Integer que esta contenido en la variable ordenamiento
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public Integer getOrdenamiento() {
		return ordenamiento;
	}
	/**
	 * @param ordenamiento : valor que se ingresa a la variable de tipo Integer
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public void setOrdenamiento(Integer ordenamiento) {
		this.ordenamiento = ordenamiento;
	}
	/**
	 * @return valor de tipo Integer que esta contenido en la variable idArea
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public Integer getIdArea() {
		return idArea;
	}
	/**
	 * @param idArea : valor que se ingresa a la variable de tipo Integer
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public void setIdArea(Integer idArea) {
		this.idArea = idArea;
	}
	/**
	 * @return valor de tipo Integer que esta contenido en la variable tipoArea
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public Integer getTipoArea() {
		return tipoArea;
	}
	/**
	 * @param tipoArea : valor que se ingresa a la variable de tipo Integer
	 *
	 * @author Homero Fidel Villanueva
	 * @since 05/03/2018
	 */
	public void setTipoArea(Integer tipoArea) {
		this.tipoArea = tipoArea;
	}
	
	
}
