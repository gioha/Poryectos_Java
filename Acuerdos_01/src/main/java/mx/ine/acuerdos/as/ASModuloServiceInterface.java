package mx.ine.acuerdos.as;

/**
 * Clase que permite manejar información del esquema de adinistración  
 * correspondiente a los módulos del sistema
 * @author INE
 * @since 13/09/2016
 */
public interface ASModuloServiceInterface {

    /**
     * Método encargado de revisar el estatus del módulo. Puede estar abrierto, cerrado o no
     * disponible.
     * @param idProceso
     * @param idDetalle
     * @param idEstado
     * @param idDistrito
     * @param idSistema
     * @param idModulo
     * @param grupo
     * @return String
     */
    public String validaModuloAbierto(Integer idProceso, Integer idDetalle, Integer idEstado,
        Integer idMunDto, Integer idSistema, Integer idModulo, String grupo, String ambitoCaptura);

}
