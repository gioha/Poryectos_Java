package mx.ine.acuerdos.dto.helper.form;

import java.io.Serializable;

public class HLPFormHome implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1551229036206507014L;
	
	
	 boolean btn_sesion;
	 boolean btn_reportes;
	 boolean btn_admin;
	 boolean btn_seguimiento;
	 private Integer numBtn;
	
	public HLPFormHome(){
		this.btn_sesion      = false;
		this.btn_reportes    = false;
		this.btn_admin       = false;
		this.btn_seguimiento = false;
		this.numBtn          = new Integer(0);
	}

	public boolean isBtn_sesion() {
		return btn_sesion;
	}

	public void setBtn_sesion(boolean btn_sesion) {
		this.btn_sesion = btn_sesion;
	}

	public boolean isBtn_reportes() {
		return btn_reportes;
	}

	public void setBtn_reportes(boolean btn_reportes) {
		this.btn_reportes = btn_reportes;
	}

	public boolean isBtn_admin() {
		return btn_admin;
	}

	public void setBtn_admin(boolean btn_admin) {
		this.btn_admin = btn_admin;
	}

	public boolean isBtn_seguimiento() {
		return btn_seguimiento;
	}

	public void setBtn_seguimiento(boolean btn_seguimiento) {
		this.btn_seguimiento = btn_seguimiento;
	}

	public Integer getNumBtn() {
		return numBtn;
	}

	public void setNumBtn(Integer numBtn) {
		this.numBtn = numBtn;
	}

}
