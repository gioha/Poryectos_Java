package mx.ine.acuerdos.dto.helper.form;

import java.io.Serializable;

/**
 * Clase contenedora de algunos de los campos de las vistas de Bandeja de Seguimiento
 * y de los elementos necesarios para soportar su funcionalidad
 * @author Miguel Ortiz
 * @since 22/02/2018
 */
public class HelperBandejaSeguimiento implements Serializable {
	private static final long serialVersionUID = 657885899229300387L;

	private Integer idNegocio;
	private Integer idTipoDocumento;

	public HelperBandejaSeguimiento() {
		this.idNegocio			= new Integer(0);
		this.idTipoDocumento	= new Integer(0);
	}

	public Integer getIdNegocio() {
		return idNegocio;
	}

	public void setIdNegocio(Integer idNegocio) {
		this.idNegocio = idNegocio;
	}

	public Integer getIdTipoDocumento() {
		return idTipoDocumento;
	}

	public void setIdTipoDocumento(Integer idTipoDocumento) {
		this.idTipoDocumento = idTipoDocumento;
	}

}