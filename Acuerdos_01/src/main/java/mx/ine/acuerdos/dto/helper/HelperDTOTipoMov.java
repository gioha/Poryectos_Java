package mx.ine.acuerdos.dto.helper;

import java.io.Serializable;

/**
 * Clase que simula el comportamiento de un DTOTipoMov, necesaria para soportar
 * la funcionalidad de la vista de Consulta y Seguimiento de Punto
 * @author Miguel Ortiz
 * @since 05/12/2017
 */
public class HelperDTOTipoMov implements Serializable {

	private static final long serialVersionUID = 2165001615867262714L;

	private Integer idTipoMovimiento;
	private String descTipoMovimiento;

	public HelperDTOTipoMov(Integer idTipoMovimiento, String descTipoMovimiento) {
		this.idTipoMovimiento = idTipoMovimiento;
		this.descTipoMovimiento = descTipoMovimiento;
	}

	public Integer getIdTipoMovimiento() {
		return idTipoMovimiento;
	}

	public void setIdTipoMovimiento(Integer idTipoMovimiento) {
		this.idTipoMovimiento = idTipoMovimiento;
	}

	public String getDescTipoMovimiento() {
		return descTipoMovimiento;
	}

	public void setDescTipoMovimiento(String descTipoMovimiento) {
		this.descTipoMovimiento = descTipoMovimiento;
	}

}
